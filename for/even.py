a = int(input("Введите десятизначное число: "))
a = str(a)
b = "1"
c = '3'
d = "5"
e = "7"
f = "9"
j = '0'
n = ""
for i in a:
    if i == b:
        continue
    elif i == c:
        continue
    elif i == d:
        continue
    elif i == e:
        continue
    elif i == f:
        continue
    elif i == j:
        continue
    n = n + i
print(n)